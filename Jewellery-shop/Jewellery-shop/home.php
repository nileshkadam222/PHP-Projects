            <p>Jewellery (jewelry in American English) is any piece of fine material used to adorn the human body. The word jewellery is derived from the word jewel, which was anglicised from the Old French jouel in around the 13th century. Further tracing leads back to the Latin word jocale, meaning plaything. Jewellery has probably been around since the dawn of man; indeed, recently found 100,000 year-old Nassarius shells that were made into beads are thought to be the oldest known jewellery.[1]</p>
            <p>Although in earlier times jewellery was created for more practical uses, such as wealth storage and pinning clothes together, in recent times it has been used almost exclusively for decoration. The first pieces of jewellery were made from natural materials, such as bone, animal teeth, shell, wood, and carved stone. Jewellery was often made for people of high importance to show their status and, in many cases, they were buried with it.</p>
            <p>Jewellery is made out of almost every material known and has been made to adorn nearly every body part, from hairpins to toe rings and many more types of jewellery. While high-quality and artistic pieces are made with gemstones and precious metals, less-costly costume jewellery is made from less-valuable materials and is mass-produced.</p>
